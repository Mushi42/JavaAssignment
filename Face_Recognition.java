    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        JFileChooser file = new JFileChooser();
        file.setCurrentDirectory(new File(System.getProperty("user.home")));
        FileNameExtensionFilter filter =  new FileNameExtensionFilter("*.Images","jpg,jpeg,gif,png");
        file.addChoosableFileFilter(filter);
        
        int result = file.showSaveDialog(null);
        if(result==JFileChooser.APPROVE_OPTION){
            File s_file = file.getSelectedFile();
            String pth = s_file.getAbsolutePath();
             jLabel7.setIcon(ResizeImage(pth));
            Users C = new Users();
       
        DataInputStream dis = null;
            try {
                dis = new DataInputStream(new FileInputStream("Data.txt"));
               
            } catch (FileNotFoundException ex) {
                Logger.getLogger(Face_Recognition.class.getName()).log(Level.SEVERE, null, ex);
            }
            try {
                while (dis.available() > 0) {
                    String str = dis.readUTF();
                    System.out.println(str);
                    
                    File ssfile;
                    ssfile = new File(str);
                    if (ssfile.exists()) {
                        ObjectInputStream in = new ObjectInputStream(new FileInputStream(str));
                        
                        C = (Users) in.readObject();
                        
                        if(C.get_path().equals(pth)){
                            jTextField1.setText(C.get_name());
                            jTextField2.setText(C.get_fname());
                            jTextField3.setText(C.get_age());
                            jTextField4.setText(C.get_gender());
                        }
                        else{
                        JOptionPane.showMessageDialog(this, "NOT Found");
                        
                    }
                    }
                    
                }   } catch (IOException ex) {
                Logger.getLogger(Face_Recognition.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Face_Recognition.class.getName()).log(Level.SEVERE, null, ex);
            }
        
       }
    }//GEN-LAST:event_jButton1ActionPerformed
    public ImageIcon ResizeImage(String ImagePath)
    {
        ImageIcon MyImage = new ImageIcon(ImagePath);
        Image img = MyImage.getImage();
        Image newImg = img.getScaledInstance(jLabel7.getWidth(), jLabel7.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon image = new ImageIcon(newImg);
        return image;
    }
